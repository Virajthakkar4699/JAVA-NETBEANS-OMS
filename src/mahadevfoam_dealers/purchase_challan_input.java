    private void jcomboproductselectFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jcomboproductselectFocusGained
        try {
            Class.forName("java.sql.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mahadev_foam","root","admin");
            Statement stmt = conn.createStatement();
            String company=jcombocompanyselect.getSelectedItem().toString();
            String companyid= "select cid from COMPANY_LIST where cname='"+company+"';";
            ResultSet rs=stmt.executeQuery(companyid);
            if(rs.next())
            {
                String c_id=rs.getString("cid");
                String PRODUCTLIST = "Select pname from product where cid='"+c_id+"';";
                ResultSet rs1 = stmt.executeQuery(PRODUCTLIST);
                jcomboproductselect.removeAllItems();
                while(rs1.next())
                {
                    jcomboproductselect.addItem(rs1.getString("Pname"));
                }
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jcomboproductselectFocusGained

